// Find the Size of int, float, double, and char //

#include <iostream>

using namespace std;

int main(){
    int a;
    float b;
    double c;
    char d;
    cout << "Size of Int : " << sizeof(a) << "\n" 
         << "Size of Float : " << sizeof(b) << "\n" 
         << "Size of Double : " << sizeof(c) << "\n" 
         << "Size of Char : " << sizeof(d) << "\n";

}